package com.example.demo.Usernoo;

public class User {
    String name;
    int id;
    String ClassNo;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getClassNo() {
        return ClassNo;
    }

    public void setClassNo(String ClassNo) {
        this.ClassNo = ClassNo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
